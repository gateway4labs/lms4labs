defined('MOODLE_INTERNAL') || die();

require_once($CFG->dirroot.'/course/moodleform_mod.php');



class mod_wlab_mod_form extends moodleform_mod {

    function definition() {

        global $DB;
	  global $COURSE;
        $mform =& $this->_form;

//-------------------------------------------------------------------------------
    /// Adding the "general" fieldset, where all the common settings are showed
        $mform->addElement('header', 'general', get_string('general', 'form'));

    /// Adding the standard "name" field
        $mform->addElement('text', 'name', get_string('name'), array('size'=>'64'));
        if (!empty($CFG->formatstringstriptags)) {
            $mform->setType('name', PARAM_TEXT);
        } else {
            $mform->setType('name', PARAM_CLEAN);
        }
        $mform->addRule('name', null, 'required', null, 'client');
        $mform->addRule('name', get_string('maximumchars', '', 255), 'maxlength', 255, 'client');
        $mform->addHelpButton('name', 'wlabname', 'wlab');

    /// Adding the standard "intro" and "introformat" fields
      ///  $this->add_intro_editor();

	  $mform->addElement('hidden', 'course', array('label'=>'Course'),array('value'=>$COURSE->id));
	  $options=array();
        $regexps=$DB->get_records('wlab_experiment_course',array('course'=>$COURSE->id));
	  foreach ($regexps as $regexp) 
	    { 
	       $reg=$DB->get_record('wlab_experiment',array('id'=>$regexp->idexp));
	 	 $options[$regexp->idexp]    = $reg->name;
	    }	
        
	  $mform->addElement('select', 'idexp', array('label'=>'Experiment'), $options);
	  

//-------------------------------------------------------------------------------
        // add standard elements, common to all modules
        $this->standard_coursemodule_elements();
//-------------------------------------------------------------------------------
        // add standard buttons, common to all modules
        $this->add_action_buttons();

    }
}
